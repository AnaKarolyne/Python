#entrada
cal = input("Informe a quantidade de calças: ")
x = float(cal)
cam = input("Informe a quantidade de camisas: ")
y = float(cam)
sap= input("Informe a quantidade de pares de sapato que possuí: ")
z = float(sap)

#processamento de dados
pos = x * y * z

#saida
print("A quantidade de possíveis combinações que pode ocorrer são: ", pos)
