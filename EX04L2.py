valor = input("Digite o primeiro número: ")
num = int(valor)
valor2 = input("Digite o segundo número: ")
num2 = int(valor2)
soma = num + num2
multiplicacao = num * num2
quociente = int(num / num2)
resto = num - (num2 * quociente)
